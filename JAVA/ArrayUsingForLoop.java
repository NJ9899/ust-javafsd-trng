package com.ust;

//Array - Using for loop
public class ArrayUsingForLoop {

	public static void main(String[] args) {
		//create an array
		int [] age= {12,4,5};
		
		//loop through the array
		//using for loop
		System.out.println("Using for loop:");
		for(int i=0;i<age.length;i++) {
			System.out.println(age[i]);
		}

	}

}
