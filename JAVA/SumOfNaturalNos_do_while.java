package com.ust;
import java.util.Scanner;

//java program to find the sum of natural numbers from 1 to 1000
public class SumOfNaturalNos_do_while {

	public static void main(String[] args) {
		int i=1,n=5;
		
		//do..while loop from 1 to 5
		do {
			System.out.println(i);
			i++;
		}while(i<=n);

	}

}
